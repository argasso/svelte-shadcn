      <h3>Börja läsa med Läskråkan</h3>
      <p>
        I serien Läskråkan samlar vi etablerade författare och illustratörer som med engagerande och
        spännande berättelser på olika nivåer kan underlätta lästräningen för barn. Serien kan
        fungera som ett första steg till att bli en van och flitig läsare.
      </p>
      <p><img src="/images/stories/argasso/uppslag_Svalan_s18-19.jpg" alt="Svalan s18-19" /></p>
      <p>
        Böckerna i Läskråkan är utvecklade i samarbete med en expert på lästräning och översatta med
        fokus på ett tydligt och enkelt språk:
      </p>
    </div>
  </div>
  <div class="row">
    <div class="col-xs-12 col-sm-8">
      <p><img src="/images/stories/argasso/Pil1.jpg" alt="Pil 1" /></p>
      <p>
        För nybörjaren som behöver enkla berättelser och stöd i läsningen. Meningarna är korta,
        orden välbekanta och inte för svåra, och det är 1–5 rader text på varje sida. Typsnittet är
        stort och de färgglada illustrationerna hjälper till att föra historien framåt.
      </p>
    </div>
  </div>
  <div class="row">
    <div class="col-xs-12 col-sm-4">
      <h4>&nbsp;Läskråkan nivå 1 har:</h4>
      <ul>
        <li>Korta, enkla meningar</li>
        <li>Välbekanta ord, som upprepas och som inte är för svåra</li>
        <li>Stort, tydligt typsnitt</li>
        <li>1–5 rader per sida</li>
        <li>Färgglada, fantasifulla bilder som hjälper till att berätta historien</li>
      </ul>
    </div>
    <div class="col-xs-12 col-sm-4">
      <h4>&nbsp;Förslag på hur du kan stödja ditt barns lästräning, nivå 1</h4>
      <ul>
        <li>
          Som en väg in i berättelsen kan du innan läsningen be ditt barn fundera på vad han eller
          hon tror att boken handlar om. Vilka ledtrådar får ni när ni tittar på bilden på framsidan
          och läser baksidestexten?
        </li>
        <li>
          Sitt med hela tiden så att du kan ge stöd och uppmuntran medan ni läser. Hjälp ditt barn
          att ljuda ord hon inte känner igen. Att du själv visar intresse och engagemang gör
          lässtunden roligare för ditt barn.
        </li>
        <li>
          När ni har läst klart berättelsen kan ni gå tillbaka till de ord som barnet tyckte var
          svåra och prata lite mer om dem. Det ökar chansen att hon kommer ihåg de nya orden och
          lättare kan bygga upp sitt ordförråd.
        </li>
      </ul>
    </div>
  </div>
  <div class="row">
    <div class="col-xs-12 col-sm-8">
      <p><img src="/images/stories/argasso/Pil2.jpg" alt="Pil 2" /></p>
      <p>
        &nbsp;Passar läsare vars självförtroende har växt lite och som börjar kunna ta till sig lite
        längre berättelser. Här krävs ett lite större ordförråd, och böckerna har en större
        variation i meningarnas uppbyggnad. Varje sida har högst 8 rader text och stora, tilltalande
        bilder.
      </p>
    </div>
  </div>
  <div class="row">
    <div class="col-xs-12 col-sm-4">
      <h4>&nbsp;Läskråkan nivå 2 har:</h4>
      <ul>
        <li>Ett lättillgängligt typsnitt</li>
        <li>Upp till 8 rader per sida</li>
        <li>Ljusa, tilltalande bilder för att öka intresset</li>
        <li>Variation i hur meningarna är uppbyggda</li>
        <li>Ett lite större ordförråd</li>
        <li>Starka teman och karaktärer</li>
      </ul>
    </div>
    <div class="col-xs-12 col-sm-4">
      <h4>Förslag på hur du kan stödja ditt barns lästräning, nivå 2</h4>
      <ul>
        <li>
          Uppmuntra ditt barn under läsningen av berättelsen. Att du själv engagerar dig och är
          positiv gör läsandet roligare för barnet.
        </li>
        <li>
          När ditt barn har läst ut boken kan du be honom eller henne att återberätta historien med
          egna ord och på sitt eget sätt. På så vis kan du se hur mycket av berättelsen hon har
          förstått under sin första genomläsning.
        </li>
        <li>
          Du kan också fråga vilken del av berättelsen som barnet gillade bäst och varför, eller
          vilken karaktär hon tyckte bäst om. Var en god lyssnare. Din nyfikenhet och dina frågor
          gör bokstunden mer minnesvärd och meningsfull för barnet.
        </li>
      </ul>
    </div>
  </div>
  <div class="row">
    <div class="col-xs-12 col-sm-8">
      <p><img src="/images/stories/argasso/Pil3.jpg" alt="" /></p>
      <p>
        Passar de barn som har lärt sig att läsa mer obehindrat. Här introduceras en boks uppdelning
        i kapitel och en mer komplicerad berättelsetråd. Språket är rikare och mer varierat och
        varje titel är rikligt illustrerad, med spännande karaktärer och ämnen. Även faktaböcker
        finns representerade på den här nivån.
      </p>
    </div>
  </div>
  <div class="row">
    <div class="col-xs-12 col-sm-4">
      <h4>Läskråkans nivå 3 har:</h4>
      <ul>
        <li>En boks uppdelning i kapitel och en mer komplicerad berättelsetråd.</li>
        <li>Ett rikt, varierat språk för att utmana och intressera den lite vanare läsaren.</li>
        <li>Rikligt med illustrationer</li>
        <li>Spännande karaktärer och ämnen</li>
      </ul>
    </div>
    <div class="col-xs-12 col-sm-4">
      <h4>Förslag på hur du kan stödja barnets lästräning, nivå 3</h4>
      <ul>
        <li>
          Uppmuntra ditt barn att läsa. Att du själv engagerar dig och är positiv hjälper till att
          göra läsandet roligt.
        </li>
        <li>
          På den här nivån kan ditt barn läsa högt för dig någorlunda flytande. I stället för att
          rätta varje misstag han eller hon gör, kan du när ditt barn har läst ut boken återvända
          till de ord barnet tyckte var svåra. På det sättet kan hon lättare behålla flytet i
          läsningen. Barnet får även bättre självförtroende om hon slipper bli rättad varje gång hon
          gör fel.
        </li>
        <li>
          Prata om de teman och händelser som finns i berättelsen tillsammans med ditt barn. Har han
          eller hon läst någon annan berättelse som påminner om den här boken, eller som har någon
          liknande huvudperson? Det här kommer att träna barnet i att jämföra olika berättelser och
          lära sig se skillnader och likheter mellan olika sätt att berätta.
        </li>
      </ul>
    </div>
  </div>
